public class Math_funcao {
    public static void main(String[] args) {
        System.out.println(Math.max(19,49));
        System.out.println(Math.min(19,49));
        System.out.println(Math.sqrt(100)); // raiz quardrada
        System.out.println(Math.log10(50)); //
        System.out.println(Math.pow(2,3)); // potencia
        System.out.println(Math.PI);
        System.out.println(Math.E);

        System.out.println(Math.round(200.4445)); // arredonda
        System.out.println(Math.random()); // entre 0 e 1
    }
}
